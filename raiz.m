function [z] = raiz(a)

    %Função que calcula a raiz quadrada de um número real;
    %
    %Para utilizá-la, digite 'raiz(x)', com o número que deseja calcular no lugar de x, e dê enter. 
    %
    %'raiz' é o nome da função;
    %'a' é o parâmetro de entrada;
    %'z' é o parâmetro de saída;
    
    z = (a^(1/2));  %Cálculo da raiz quadrada;
end



